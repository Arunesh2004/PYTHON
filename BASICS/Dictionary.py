#In dictionary the data is stored in key-value pairs.

numbers = {"num1": 1, "num2": 2, "num3": 3, "num4": 4, "num5": 5}

print(numbers["num2"])

print(" ")


# To add an element to the dictionary :-

numbers["num6"] = 6

print(numbers)

print(" ")

# To remove an element from the dictionary :-

del numbers["num3"]

print(numbers)

print(" ")

# To update an element in the dictionary :-

numbers["num4"] = 40

print(numbers)